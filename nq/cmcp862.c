static NQ_INT
cp862UnicodeToAnsi(
    NQ_CHAR* aStr,
    const NQ_WCHAR* wStr,
    NQ_INT inLength,
    NQ_INT outLength
    )
{
    const NQ_WCHAR* pW = wStr;
    NQ_CHAR* pA = aStr;
    NQ_INT length = 0;
    NQ_BOOL ignoreInLength = (inLength == -1);
    NQ_BOOL ignoreOutLength = (outLength == -1);

    inLength /= 2;

    if (aStr && wStr)
    {
        for ( ; *pW && (ignoreInLength || inLength) && (ignoreOutLength || (length < outLength)); pA++, pW++, inLength--, length++ )
        {
            /* extract Msb in wStr */
            NQ_BYTE wLsb, wMsb;
            cmSplitUnicode (*pW, &wMsb, &wLsb);

            *pA = 0x00;

            switch (wMsb)
            {
            case 0x00:
                if (wLsb < 0xA0)  *pA = (NQ_CHAR)wLsb;
                else  if (wLsb < 0xf8) *pA = (NQ_CHAR)u2a_00[wLsb-0xA0];
                break;
            case 0x05:
                if (0xd0 <= wLsb && wLsb <= 0xea) *pA = (NQ_CHAR)u2a_05[wLsb-0xd0];
                break;
            case 0x01:
                if (wLsb == 0x92) *pA = (NQ_CHAR)0x9f;
                break;
            case 0x03:
                if (0x93 <= wLsb && wLsb <= 0xc6) *pA = (NQ_CHAR)u2a_03[wLsb-0x93];
                break;
            case 0x22:
                if (0x19 <= wLsb && wLsb <= 0x65) *pA = (NQ_CHAR)u2a_22[wLsb-0x19];
                break;
            case 0x25:
                if (wLsb <= 0xa0) *pA = (NQ_CHAR)u2a_25[wLsb];
                break;
            case 0x20:
                switch(wLsb)
                {
                case 0xa7: *pA = (NQ_CHAR)0x9e; break;
                case 0x7f: *pA = (NQ_CHAR)0xfc;
                }
                break;
            case 0x23:
                switch(wLsb)
                {
                case 0x10: *pA = (NQ_CHAR)0xa9; break;
                case 0x20: *pA = (NQ_CHAR)0xf4; break;
                case 0x21: *pA = (NQ_CHAR)0xf5;
                }
                break;
            }
            if (*pA == 0x00) /* wrong unicode */
                break;
        }
    }
    else
    {
        if (!aStr)
        {
            length = 0;
            goto Exit;
        }
    }
    /* not enough space in provided buff */
    if (ignoreOutLength || (length < outLength))
        *pA = '\0';

Exit:
    return length;
}

