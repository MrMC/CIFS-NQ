static NQ_INT
cp850UnicodeToAnsi(
    NQ_CHAR* aStr,
    const NQ_WCHAR* wStr,
    NQ_INT inLength,
    NQ_INT outLength
    )
{
    const NQ_WCHAR* pW = wStr;
    NQ_CHAR* pA = aStr;
    NQ_INT length = 0;
    NQ_BOOL ignoreInLength = (inLength == -1);
    NQ_BOOL ignoreOutLength = (outLength == -1);

    inLength /= 2;

    if (aStr && wStr)
    {
        for ( ; *pW && (ignoreInLength || inLength) && (ignoreOutLength || (length < outLength)); pA++, pW++, inLength--, length++ )
        {
            /* extract Msb in wStr */
            NQ_BYTE wLsb, wMsb;
            cmSplitUnicode (*pW, &wMsb, &wLsb);

            *pA = 0x00;

            switch (wMsb)
            {
            case 0x00:
                if (wLsb < 0xA0)  *pA = (NQ_CHAR)wLsb;
                else *pA = (NQ_CHAR)u2a_00[wLsb-0xA0];
                break;
            case 0x01:
                switch (wLsb)
                {
                case 0x31: *pA = (NQ_CHAR)0xd5; break;
                case 0x92: *pA = (NQ_CHAR)0x9f;
                }
                break;
            case 0x20:
                if (wLsb == 0x17) *pA = (NQ_CHAR)0xf2;
                break;
            case 0x25:
                if (wLsb <= 0xa0) *pA = (NQ_CHAR)u2a_25[wLsb];
                break;
            }
            if (*pA == 0x00) /* wrong unicode */
                break;
        }
    }
    else
    {
        if (!aStr)
            return 0;
    }
    /* not enough space in provided buff */
    if (ignoreOutLength || (length < outLength))
        *pA = '\0';

    return length;
}
